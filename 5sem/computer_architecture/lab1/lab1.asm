section .data                       ; секция инициализированных данных
    String1 db 13, 10, "____", 0    ; строка с символами возврата каретки(13) и перевода строки(10)
    data_size dw 5                  ; переменная размером 2 байта со значением 5 (аналог ds из MASM32)
    align 4                         ; выравнивание следующей метки на границу 4 байта
    result dd 0                     ; переменная размером 4 байта для хранения результата (инициализирована 0)
    p dq 17.5                       ; переменная размером 8 байт со значением 17.5
    ten dt 183333.5                 ; переменная размером 10 байт со значением 183333.5

section .text                       ; секция кода
    global _start                   ; объявление точки входа для линкера

_start:                             ; метка начала программы
    xor ecx, ecx                    ; обнуление регистра ecx
    movzx ecx, word [data_size]     ; загрузка значения из data_size в регистр ecx с расширением нулём
    add ecx, 5                      ; увеличение значения в регистре ecx на 5
    mov eax, ecx                    ; копирование значения из ecx в eax (для операции деления)
    mov edx, 0                      ; обнуление регистра edx (обязательно для операции деления)
    mov ebx, 2                      ; загрузка значения 2 в регистр ebx (делитель)
    div ebx                         ; деление eax на ebx, результат в eax, остаток в edx
    mov [result], eax               ; сохранение результата деления в переменную result

    mov eax, 1                      ; номер системного вызова exit
    xor ebx, ebx                    ; код возврата 0 (успешное завершение)
    int 0x80                        ; вызов прерывания для завершения программы
